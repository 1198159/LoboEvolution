package org.loboevolution.info;


import java.io.Serializable;

public class MetaInfo implements Serializable {

	private static final long serialVersionUID = 1L;
	private String name;
	private String content;
	private String description;
	private String charset;
	private String itemprop;
	private String property;
	private String httpEqui;

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the content
	 */
	public String getContent() {
		return content;
	}

	/**
	 * @param content
	 *            the content to set
	 */
	public void setContent(String content) {
		this.content = content;
	}

	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}

	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}

	/**
	 * @return the charset
	 */
	public String getCharset() {
		return charset;
	}

	/**
	 * @param charset the charset to set
	 */
	public void setCharset(String charset) {
		this.charset = charset;
	}

	/**
	 * @return the itemprop
	 */
	public String getItemprop() {
		return itemprop;
	}

	/**
	 * @param itemprop the itemprop to set
	 */
	public void setItemprop(String itemprop) {
		this.itemprop = itemprop;
	}

	/**
	 * @return the property
	 */
	public String getProperty() {
		return property;
	}

	/**
	 * @param property the property to set
	 */
	public void setProperty(String property) {
		this.property = property;
	}

	/**
	 * @return the httpEqui
	 */
	public String getHttpEqui() {
		return httpEqui;
	}

	/**
	 * @param httpEqui the httpEqui to set
	 */
	public void setHttpEqui(String httpEqui) {
		this.httpEqui = httpEqui;
	}

}
